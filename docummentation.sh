#! /bin/bash

echo "              ***********************     Help    ***************************


bash script.sh [options] [paramètres]

+) Paramètres: fichiers , dossiers
+) Options : 
    -h (help): Affiche une documentation détaillée du programme.
    -f (fork): exécution par création de sous-processus avec fork.
    -t (thread): Permet une exécution par threads.
    -s (subshell): Exécute le programme dans un sous-shell.
    -l (log): spécifier un répertoire pour le stockage du fichier de journalisation.
    -r (restore): Réinitialise les paramètres par défaut.
    -d (destination) : indiquer le dossier de destination (sera le dernier paramètre)."
